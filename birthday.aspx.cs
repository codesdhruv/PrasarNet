        public void grdbind()
        {
            ds = cmm.RunSpReturnDS("IT_BirthDay_RSS", "HRITConnectionString");
            grdbday.DataSource = ds;
            grdbday.DataBind();
        }

        protected void lnklogout_Click(object sender, EventArgs e)
